public class Tablero {
	
	public int filas;
	public int columnas;
	
 public Tablero(int filas, int columnas) {
	 
 }

public int getFilas() {
	return filas;
}

public void setFilas(int filas) {
	this.filas = filas;
}

public int getColumnas() {
	return columnas;
}

public void setColumnas(int columnas) {
	this.columnas = columnas;
}
 
}
